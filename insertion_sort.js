// This is using the shift steps
function insertionSort(arr) {
  for (i = 1; i < arr.length; i++) {
    let temp = arr[i];
    let j = i - 1;
    while (j >= 0 && arr[j] > temp) {
      arr[j + 1] = arr[j];
      j--;
    }
    arr[j + 1] = temp;
  }
  return arr;
}

let arr = [11, 9, 3, 6, 2, 9, 9, 3, 0, 5];
console.log(insertionSort(arr));

